function sum(a, b) {
  return a + b
}

function subtraction(a, b) {
  return a - b
}

function multiplication(a, b) {
  return a * b
}

function division(a, b) {
  return a / b
}

module.exports = {
  sum,
  subtraction,
  multiplication,
  division
}

